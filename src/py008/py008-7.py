def main():
    # 7. 変数に型の違うものを入れてみる
    hensu1 = 1
    hensu2 = 2

    # 上記の数字が表示される
    print(hensu1)
    print(hensu2)

    # 変数に型の違う値を入れてみる
    hensu1 = "hoge"
    hensu2 = "foo"

    # 上記の文字列が表示される
    print(hensu1)
    print(hensu2)


# これ以下はおまじない（とりあえず丸暗記）
if __name__ == "__main__":
    main()
