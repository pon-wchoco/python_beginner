def main():
    # 3. 変数に値を入れてみる
    # 変数を定義
    hensu1 = None
    hensu2 = None

    # これはNoneと表示される
    print(hensu1)
    print(hensu2)

    # 変数に値を入れてみる
    hensu1 = 1
    hensu2 = 2

    # これは変数に入れた値が表示される
    print(hensu1)
    print(hensu2)


# これ以下はおまじない（とりあえず丸暗記）
if __name__ == "__main__":
    main()
